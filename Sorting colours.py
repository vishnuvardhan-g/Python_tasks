#Program to sort the list of colours

def sort_colors(colors):
    return sorted(colors)

if __name__ == '__main__':
    colors = ["Blue", "Red", "Green", "Yellow", "Orange", "Purple", "Pink"]
    sorted_colors = sort_colors(colors)
    print("Original colors:")
    print(colors)
    print("sorted colors:")
    print(sorted_colors)
